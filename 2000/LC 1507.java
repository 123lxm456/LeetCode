class Solution {
    public String reformatDate(String date) {
        String[] arr = date.split(" ");
        StringBuilder str = new StringBuilder();
        Map<String, String> map = new HashMap<>();
        map.put("Jan", "01");
        map.put("Feb", "02");
        map.put("Mar", "03");
        map.put("Apr", "04");
        map.put("May", "05");
        map.put("Jun", "06");
        map.put("Jul", "07");
        map.put("Aug", "08");
        map.put("Sep", "09");
        map.put("Oct", "10");
        map.put("Nov", "11");
        map.put("Dec", "12");
        str.append(arr[2] + "-");
        str.append(map.get(arr[1]) + "-");
        if(arr[0].length() == 3){
            str.append("0" + arr[0].charAt(0));
        }
        else
        {
            str.append(arr[0].substring(0,2));
        }
        return str.toString();
    }
}

